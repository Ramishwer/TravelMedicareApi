package com.travel.api.quote.reponse;

public class QuickQuoteResponse {

	private double costWithPreExisting;
	private double costwithoutPreExisting;
	
	public double getCostWithPreExisting() {
		return costWithPreExisting;
	}
	public void setCostWithPreExisting(double costWithPreExisting) {
		this.costWithPreExisting = costWithPreExisting;
	}
	public double getCostwithoutPreExisting() {
		return costwithoutPreExisting;
	}
	public void setCostwithoutPreExisting(double costwithoutPreExisting) {
		this.costwithoutPreExisting = costwithoutPreExisting;
	}
	
	
}
